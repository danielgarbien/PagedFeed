import Foundation
import UIKit

extension UIColor {
    
    static func myGreyColor() -> UIColor {
        return UIColor(white: 83.0 / 255.0, alpha: 1.0)
    }
    
    static func myLightGreyColor() -> UIColor {
        return UIColor(white: 235.0 / 255.0, alpha: 1.0)
    }
    
    static func myRedColor() -> UIColor {
        return UIColor(red: 190.0 / 255.0, green: 44.0 / 255.0, blue: 29.0 / 255.0, alpha: 1.0)
    }
}
